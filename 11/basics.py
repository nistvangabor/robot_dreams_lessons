
#real life example: egy telken felépítünk 1 ikerházat.

# program: a ház építési terve, nem használ semmilyen erőforrást amíg nem kezdődik el az építkezés
# process: a ház tényleges építése, már erőforrásokat használva. lehet az ikerház két fele két külön process.
# thread: festés, falazás, elektronika stb egy processen belül, tehát egy ház-oldal építésén belül.